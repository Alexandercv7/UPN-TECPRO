    private void JBtnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBtnMostrarActionPerformed
        String nombres = jTxtNombre.getText();
        String categoria = jCboCategoria.getSelectedItem().toString();
        String estudios = jCboEstudios.getSelectedItem().toString();
        int horas = Integer.parseInt(jSpnAñosServ.getValue().toString());
        
        Docente profesor = new Docente();

        profesor.setNombre(nombres);
        profesor.setCategoria(categoria);
        profesor.setEstudios(estudios);
        profesor.setHoras(horas);
        String resumen = jTxtAreaLista.getText();

        jLblCodigo.setText(profesor.getCodigo());
        jLblNombre.setText(profesor.getNombre());
        jLblEstudios.setText(profesor.getEstudios());
        jLblCategoria.setText(profesor.getCategoria());
        
        jLblPagoParcial.setText("Pago Parcial : S/."+ redondeo(profesor.pagHora()));
        jLblBonificacion.setText("Bonificación : S/."+ redondeo(profesor.bonificacion()));
        jLblDescuento.setText("Descuento : S/." + redondeo(profesor.descuento()));
        jLblSueldoFinal.setText("Sueldo Final : S/." + redondeo(profesor.sueldoFinal()));
        jTxtAreaLista.setText(resumen + profesor.mostrarDatos());
        
        limpiar(0);
    }//GEN-LAST:event_JBtnMostrarActionPerformed


    public void limpiar(int valor){
        if(valor==1){
            jLblCodigo.setText("");
            jLblNombre.setText("");
            jLblEstudios.setText("");
            jLblCategoria.setText("");

            jLblPagoParcial.setText("");
            jLblBonificacion.setText("");
            jLblDescuento.setText("");
            jLblSueldoFinal.setText("");
            jTxtAreaLista.setText("");

            jCboCategoria.setSelectedIndex(0);
            jCboEstudios.setSelectedIndex(0);
            jTxtNombre.setText("");
            jSpnAñosServ.setValue(0);

            jTxtNombre.requestFocus();
        }else{
            jCboCategoria.setSelectedIndex(0);
            jCboEstudios.setSelectedIndex(0);
            jTxtNombre.setText("");
            jSpnAñosServ.setValue(0);

            jTxtNombre.requestFocus();
        }
    }
    
    public String redondeo(double valor){
        DecimalFormat df = new DecimalFormat("#.##");
        return df.format(valor);
    }
    
